function loadTableUI() {
    GetDrivers(function (result) {
        var table = "";
        var id = 1;
        $.each(result, function (index, zone) {
            table += "<tr>";
            table += "<td>" + id++ + "</td>";
            table += "<td>" + zone.name + "</td>";
            table += "<td>" + zone.code + "</td>";
            table += "<td><button id='" + zone.id + "' type='button' class='btn btn-block btn-success btn-xs btnAction'>Select</button></td>";
            table += "</tr>";
        });
        $('#tblZones tbody').html(table);
        $("#tblZones").DataTable();
    });
}

